    t(key) {
        return (this.dict[this.current] && this.dict[this.current][key])
            || (this.dict['en'] && this.dict['en'][key])
            || key;
    },

    set(lang) {
        if (!this.dict[lang]) return;
        this.current = lang;
        localStorage.setItem('pm-lang', lang);
        document.documentElement.setAttribute('lang', lang);
        this.apply();
    },

    toggle() {
        this.set(this.current === 'en' ? 'zh' : 'en');
    },

    apply() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (key) el.textContent = this.t(key);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (key) el.setAttribute('placeholder', this.t(key));
        });
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            if (key) el.setAttribute('title', this.t(key));
        });
        document.querySelectorAll('option[data-i18n-option]').forEach(el => {
            const key = el.getAttribute('data-i18n-option');
            if (key) el.textContent = this.t(key);
        });
    },
